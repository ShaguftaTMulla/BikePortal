package dto;



public class BikeNews {
	
	private int newsId;
	private String newsBox;
	private String newsHead;
	private String newsRef;
	public int getNewsId() {
		return newsId;
	}
	public void setNewsId(int newsId) {
		this.newsId = newsId;
	}
	public String getNewsBox() {
		return newsBox;
	}
	public void setNewsBox(String newsBox) {
		this.newsBox = newsBox;
	}
	public String getNewsHead() {
		return newsHead;
	}
	public void setNewsHead(String newsHead) {
		this.newsHead = newsHead;
	}
	public String getNewsRef() {
		return newsRef;
	}
	public void setNewsRef(String newsRef) {
		this.newsRef = newsRef;
	}
	public BikeNews() {
		super();
	}
	
	
	
	
}
